    <!-- Script pour animer les accordéons FAQ -->
    <script>
        function toggleFaq(button) {
            // Récupère le contenu associé au bouton
            const content = button.nextElementSibling;
            const icon = button.querySelector('svg');
            
            // Vérifie si le contenu est ouvert (max-height n'est pas 0)
            const isOpen = content.classList.contains('max-h-96');
            
            // Ferme tous les autres accordéons
            const allContents = document.querySelectorAll('#accordion-faq > div > div:not(button + div)');
            const allIcons = document.querySelectorAll('#accordion-faq svg');
            
            allContents.forEach(item => {
                if (item !== content) {
                    item.classList.remove('max-h-96', 'opacity-100');
                    item.classList.add('max-h-0', 'opacity-0');
                }
            });
            
            allIcons.forEach(item => {
                if (item !== icon) {
                    item.classList.remove('rotate-180');
                }
            });
            
            // Bascule la visibilité du contenu cliqué
            if (!isOpen) {
                content.classList.remove('max-h-0', 'opacity-0');
                content.classList.add('max-h-96', 'opacity-100');
                icon.classList.add('rotate-180');
            } else {
                content.classList.remove('max-h-96', 'opacity-100');
                content.classList.add('max-h-0', 'opacity-0');
                icon.classList.remove('rotate-180');
            }
        }
    </script>
